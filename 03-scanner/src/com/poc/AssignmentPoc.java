package com.poc;
public class AssignmentPoc {

	public static void main(String[] args) {
        int n = 10;

        // Assignment Operations
        n += 5;
        System.out.println("a += 5: " + n);

        n -= 3;
        System.out.println("a -= 3: " + n);

        n *= 2;
        System.out.println("a *= 2: " + n);

        n /= 4;
        System.out.println("a /= 4: " + n);

        n %= 3;
        System.out.println("a %= 3: " + n);
    }

}
